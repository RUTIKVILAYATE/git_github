# What is Git?

# Git is a distributed-version control system for tracking changes in source code during software development

# It is designed for coordinating work amoung programmers, but it can be used to track changes in any set of files.

# Its goal include speed, data integrity, and support for distributed, non-linear workflows



# Version Control is a system that records changes to a file or a set of files over time so that you can recall specific versions later

# Example:- Excel file 

